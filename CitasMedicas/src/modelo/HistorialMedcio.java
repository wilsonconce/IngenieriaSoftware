package modelo;

import java.util.Date;

public class HistorialMedcio {

	private int hm_id;
	private Date hm_fecha_actualizada;

	public int getHm_id() {
		return hm_id;
	}

	public void setHm_id(int hm_id) {
		this.hm_id = hm_id;
	}

	public Date getHm_fecha_actualizada() {
		return hm_fecha_actualizada;
	}

	public void setHm_fecha_actualizada(Date hm_fecha_actualizada) {
		this.hm_fecha_actualizada = hm_fecha_actualizada;
	}

}
