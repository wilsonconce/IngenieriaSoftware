package ec.edu.ups.modelo;

public class Paciente {
	
	private int pac_id;
	private int pac_per_id;
	private String pac_fecha_registro;
	
	
	public int getPac_id() {
		return pac_id;
	}
	public void setPac_id(int pac_id) {
		this.pac_id = pac_id;
	}
	public int getPac_per_id() {
		return pac_per_id;
	}
	public void setPac_per_id(int pac_per_id) {
		this.pac_per_id = pac_per_id;
	}
	public String getPac_fecha_registro() {
		return pac_fecha_registro;
	}
	public void setPac_fecha_registro(String pac_fecha_registro) {
		this.pac_fecha_registro = pac_fecha_registro;
	}
	
	
	

}
